#define HEX_DIGIT(n)  ((n < 10) ? n + '0' : n - 10 + 'a')

void print_sbox(uint8_t sbox[][16])
{
	int i, j;

	printf("   ");
	for (i = 0; i < 16; i++)
		printf("%2c ", HEX_DIGIT(i));
	printf("\n");

	for (i = 0; i < 16; i++) {
		printf("%c  ", HEX_DIGIT(i));
		for (j = 0; j < 16; j++)
			printf("%02x ", sbox[i][j]);
		printf("\n");
	}
}

inline uint8_t sbox_lookup(uint8_t sbox[][16], uint8_t c)
{
	return sbox[(c >> 4) & 0xf][(c >> 0) & 0xf];
}

inline uint8_t *mixcol_tab_lookup(
		uint8_t *mc_tab[][16], uint8_t c)
{
	return mc_tab[(c >> 4) & 0xf][(c >> 0) & 0xf];
}

void verify_sboxes()
{
	int i, j;
	char c;

	for (i = 0; i < 16; i++) {
		for (j = 0; j < 16; j++) {
			c = sbox_lookup(inv_sbox, sbox[i][j]);
			if (((c >> 4) & 0xf) != i || (c & 0xf) != j)
				printf("%d, %d\n", i, j);
		}
	}
}

inline uint8_t xtime(uint8_t x)
{
	uint8_t res = x << 1;

	if (x & 0x80)
		res ^= 0x1b;
	return res;
}

inline uint8_t mul(uint8_t x, int n)
{
	uint8_t res = 0, curr = x;
	int i;

	for (i = 0; i < 8; i++) {
		if ((n >> i) & 0x1)
			res ^= curr;
		curr = xtime(curr);
	}
	return res;
}

int mixcol_multipliers[] = {2, 1, 1, 3};
int inv_mixcol_multipliers[] = {14, 9, 13, 11};

inline void generate_mixcol_tab_row(int multipliers[], uint8_t idx)
{
	printf("\"\\x%02x\\x%02x\\x%02x\\x%02x\"",
			mul(idx, multipliers[0]), mul(idx, multipliers[1]),
			mul(idx, multipliers[2]), mul(idx, multipliers[3]));
}

void generate_mixcol_tab(int inv)
{
	int *mc_multipliers;
	int i, j, idx;

	if (!inv) {
		printf("uint8_t *mixcol_tab[][16] = {\n");
		mc_multipliers = mixcol_multipliers;
	} else {
		printf("uint8_t *inv_mixcol_tab[][16] = {\n");
		mc_multipliers = inv_mixcol_multipliers;
	}

	for (i = 0; i < 16; i++) {
		printf("\t\t{\n");
		for (j = 0; j < 16; j += 2) {
			idx = (i << 4) + j;
			printf("\t\t\t");
			generate_mixcol_tab_row(mc_multipliers, idx);
			printf(", ");
			generate_mixcol_tab_row(mc_multipliers, idx + 1);
			printf(",\n");
		}
		printf("\t\t},\n");
	}

	printf("\t};\n");
}

void generate_comb_mixcol_tab_row(uint8_t *col)
{
	int i = 0, j;
	
	printf("\"");
	do {
		for (j = 0; j < 4; j++)
			printf("\\x%02x", col[(i + j) & 0x3]);
		i = (i - 1) & 0x3;
		if (i == 2)
			printf("\"\n\t\t\t\t\"");
	} while (i != 0);
	printf("\"");
}

void generate_comb_mixcol_tab(int inv)
{
	uint8_t *col;
	uint8_t (*sb)[16], *(*mc_tab)[16];
	int i, j;

	if (!inv) {
		printf("uint8_t *comb_mixcol_tab[][16] = {\n");
		sb = sbox;
		mc_tab = mixcol_tab;
	} else {
		printf("uint8_t *comb_inv_mixcol_tab[][16] = {\n");
		sb = inv_sbox;
		mc_tab = inv_mixcol_tab;
	}

	for (i = 0; i < 16; i++) {
		printf("\t\t{\n");
		for (j = 0; j < 16; j += 2) {
			printf("\t\t\t");
			col = mixcol_tab_lookup(mc_tab, sbox_lookup(sb, (i << 4) + j));
			generate_comb_mixcol_tab_row(col);
			printf(",\n\t\t\t");
			col = mixcol_tab_lookup(mc_tab, sbox_lookup(sb, (i << 4) + j + 1));
			generate_comb_mixcol_tab_row(col);
			printf(",\n");
		}
		printf("\t\t},\n");
	}

	printf("\t};\n");
}

